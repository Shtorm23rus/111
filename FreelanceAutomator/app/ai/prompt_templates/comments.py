COMMENT_SYSTEM_PROMPT = """Вы - эксперт по написанию естественных комментариев для различных платформ.
Ваша задача - создавать комментарии, которые выглядят как настоящие сообщения реальных пользователей.
Комментарии должны быть:
- Краткими и по существу
- Релевантными теме
- Естественными по стилю
- Разнообразными по структуре
"""

def get_comment_prompt(topic: str, context: str = None, length: str = "short") -> str:
    length_guide = {
        "short": "1-2 предложения",
        "medium": "2-4 предложения",
        "long": "4-6 предложений"
    }
    
    prompt = f"""Тема для комментария: {topic}
    
Длина комментария: {length_guide.get(length, "2-4 предложения")}"""
    
    if context:
        prompt += f"\nКонтекст: {context}"
    
    prompt += "\n\nНапишите естественный комментарий."
    
    return prompt
