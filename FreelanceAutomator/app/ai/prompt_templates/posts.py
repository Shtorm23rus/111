POST_SYSTEM_PROMPT = """Вы - эксперт по созданию вовлекающего контента для социальных сетей и блогов.
Ваша задача - создавать посты, которые привлекают внимание и выглядят естественно.
Посты должны быть:
- Вовлекающими и интересными
- Структурированными и легко читаемыми
- Соответствующими целевой аудитории
- Уникальными и не шаблонными
"""

def get_post_prompt(topic: str, platform: str = "generic", target_audience: str = None) -> str:
    platform_guides = {
        "facebook": "формальный, но дружелюбный стиль",
        "instagram": "визуальный, короткий, с хэштегами",
        "linkedin": "профессиональный и деловой стиль",
        "twitter": "краткий и динамичный стиль",
        "generic": "универсальный стиль"
    }
    
    prompt = f"""Создайте пост на тему: {topic}
    
Платформа: {platform}
Стиль: {platform_guides.get(platform, "универсальный стиль")}"""
    
    if target_audience:
        prompt += f"\nЦелевая аудитория: {target_audience}"
    
    prompt += "\n\nСоздайте привлекательный и естественный пост."
    
    return prompt
